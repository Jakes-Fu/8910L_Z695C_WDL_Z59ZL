void getCertFromStore(int id, unsigned char **cert_ptr, int *len)
{
#ifdef VM_TCK_ENTRY
	JWE_LOG(getCertFromStore, ("read CER id = %d", id));
	if(id ==0 )
	{
    		KJavaDevFileHandle fp;
		int err;
		signed long readLen;
		JWE_LOG(getCertFromStore, ("get Cert from card(e)"));
		SCI_MEMSET(tckCerBuf, 0, sizeof(tckCerBuf));
		fp = kjava_dfs_open("E:\\tck.cer", &err);
		if(fp)
		{
			JWE_LOG(getCertFromStore, ("file open!"));
			kjava_dfs_read(tckCerBuf, 0, 2048, &readLen, fp);
			kjava_dfs_close(fp);
			JWE_LOG(getCertFromStore, ("read lens = %d", readLen));
			*cert_ptr = (unsigned char*)tckCerBuf;
			*len = readLen;	
		}
	}
#else	
    	*cert_ptr = (unsigned char *)calist[id].cert_data;

    	*len = calist[id].cert_data_len;
#endif		
}
